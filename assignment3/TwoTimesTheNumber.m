function [doubleIt] = TwoTimesTheNumber(num1)
% This function will take one number as an input and return a value that is
% 2 times the number as an output
%
% Inputs = 1 number
% Outputs = twice the number input
% Written by Kaitlyn Wojciechowski
% Tested on Matlab 2024a
% ok on MacOS Monterey version 12.2.1
doubleIt = num1*2
end